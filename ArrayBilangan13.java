public class ArrayBilangan13 {
    public static void main(String[] args) {
        int[] bil = new int [4];

        bil[0] = 5 ;
        bil[1] = 13 ;
        bil[2] = -7 ;
        bil[3] = 17 ;

        System.out.println(bil[0]);
        System.out.println(bil[13]);
        System.out.println(bil[-7]);
        System.out.println(bil[17]);
    }
}